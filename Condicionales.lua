-- Condicional if
local numero = 10
if numero > 5 then
    print("El número es mayor que 5")
end

-- Condicional if-else
local edad = 18
if edad >= 18 then
    print("Eres mayor de edad")
else
    print("Eres menor de edad")
end

-- Condicional if-elseif-else
local nota = 85
if nota >= 90 then
    print("Excelente")
elseif nota >= 80 then
    print("Muy bien")
elseif nota >= 70 then
    print("Bien")
else
    print("Necesitas mejorar")
end

-- Condicional con booleano
local esVerdadero = true
if esVerdadero then
    print("La condición es verdadera")
else
    print("La condición es falsa")
end